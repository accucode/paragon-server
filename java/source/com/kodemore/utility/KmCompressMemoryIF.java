public interface KmCompressMemoryIF
{
    /**
     * This is used to compress memory.  Most pages and controls are
     * created when the servlet container is initialized and permanently
     * kept in memory. Because of this, compressing memory for the long
     * lived objects is warranted.
     *
     * Compression typically includes operations such as interning strings,
     * and trimming padding from static collections.
     */
    void compressMemory();
}
